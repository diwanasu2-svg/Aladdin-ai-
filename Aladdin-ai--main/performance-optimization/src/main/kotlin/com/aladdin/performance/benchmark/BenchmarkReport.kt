package com.aladdin.performance.benchmark

import java.util.Date

enum class BenchmarkRating { EXCELLENT, GOOD, FAIR, POOR }

data class BenchmarkResult(
    val name: String,
    val score: Double,
    val unit: String,
    val rating: BenchmarkRating,
    val durationMs: Long,
    val target: Double? = null,
    val metTarget: Boolean = target == null || score >= target
)

data class PerformanceBenchmarkReport(
    val generatedAtMs: Long = System.currentTimeMillis(),
    val deviceModel: String = android.os.Build.MODEL,
    val androidSdk: Int = android.os.Build.VERSION.SDK_INT,
    val results: List<BenchmarkResult>,
    val startupMs: Long = 0L,
    val startupMetTarget: Boolean = startupMs in 1..<3001
) {
    val overallRating: BenchmarkRating get() {
        val poor = results.count { it.rating == BenchmarkRating.POOR }
        val fair = results.count { it.rating == BenchmarkRating.FAIR }
        return when {
            poor > 2           -> BenchmarkRating.POOR
            poor > 0 || fair > 3 -> BenchmarkRating.FAIR
            fair > 0           -> BenchmarkRating.GOOD
            else               -> BenchmarkRating.EXCELLENT
        }
    }
    val allTargetsMet get() = results.all { it.metTarget } && startupMetTarget

    fun toMarkdown(): String = buildString {
        appendLine("# Aladdin Performance Benchmark Report")
        appendLine("**Generated:** ${Date(generatedAtMs)}")
        appendLine("**Device:** $deviceModel (SDK $androidSdk)")
        appendLine("**Overall Rating:** $overallRating ${if (allTargetsMet) "✅ All targets met" else "⚠️ Some targets missed"}")
        appendLine()
        if (startupMs > 0) {
            val icon = if (startupMetTarget) "✅" else "❌"
            appendLine("## Startup")
            appendLine("- Time: **${startupMs}ms** (target < 3000ms) $icon")
            appendLine()
        }
        appendLine("## Benchmarks")
        appendLine("| Test | Score | Unit | Rating | Target | Status |")
        appendLine("|------|-------|------|--------|--------|--------|")
        results.forEach { r ->
            val icon = if (r.metTarget) "✅" else "❌"
            val target = r.target?.let { "${"%.1f".format(it)} ${r.unit}" } ?: "—"
            appendLine("| ${r.name} | ${"%.2f".format(r.score)} | ${r.unit} | ${r.rating} | $target | $icon |")
        }
        appendLine()
        appendLine("*Generated by Aladdin Performance Optimization Module*")
    }
}
